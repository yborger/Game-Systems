carsMade=0

function Car:initialize()
  carsMade = carsMade+1
  self.x=70 --also position
  self.y=10+(carsMade*9) -- position
  self.vx=0 -- also velocity
  self.vy=0 -- velocity
  self.a=80 -- angle
