function Car:update(amt, go)
  self.a=angleAdd(self.a, amt)
  if(go) then
    self.ax, self.ay = vector(ACCEL_VALUE,self.a)
    self.vx=self.vx+self.ax
    self.vy=self.vy+self.ay
  end
end

